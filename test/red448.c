#define RUN_RFC_VECTOR(seed_, pub_, msg_, msgsize_, sig_) do { \
  RCryptoKey * priv, * pub_k; \
  const ruint8 * pub_borrow = NULL; \
  rsize pub_size = 0; \
  ruint8 sig_out[114]; \
  rsize sig_outsize = sizeof (sig_out); \
  priv = r_ed448_priv_key_new (seed_, 57); \
  r_assert_cmpptr (priv, !=, NULL); \
  r_assert (r_ed448_key_get_pub (priv, &pub_borrow, &pub_size)); \
  r_assert_cmpuint (pub_size, ==, 57); \
  r_assert_cmpmem (pub_borrow, ==, pub_, 57); \
  r_assert_cmpint (r_ed448_sign (priv, msg_, msgsize_, sig_out, &sig_outsize), \
      ==, R_CRYPTO_OK); \
  r_assert_cmpuint (sig_outsize, ==, 114); \
  r_assert_cmpmem (sig_out, ==, sig_, 114); \
  pub_k = r_ed448_pub_key_new (pub_, 57); \
  r_assert_cmpptr (pub_k, !=, NULL); \
  r_assert_cmpint (r_ed448_verify (pub_k, msg_, msgsize_, sig_, 114), \
      ==, R_CRYPTO_OK); \
  r_crypto_key_unref (priv); \
  r_crypto_key_unref (pub_k); \
} while (0)

